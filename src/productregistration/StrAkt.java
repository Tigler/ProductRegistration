    public StrAkt(int PK, int addOrUpdate, int pkAkt) {
        initComponents();
        pkTovar = new ArrayList<>();
        valuesTovar = new ArrayList<>();
        this.PK = PK;
        this.addOrUpdate = addOrUpdate;
        this.pkAkt = pkAkt;
        ResultSet resSet = null;
        try {
            resSet = ProductRegistration.st.executeQuery("select pk_tovar,"
                    + " nametovar from tovar"
            );
            while (resSet.next()) {
                pkTovar.add(resSet.getString(1));
                valuesTovar.add(resSet.getString(2));
            }
            jComboBoxTovar.setModel(new DefaultComboBoxModel(valuesTovar.toArray()));
            jComboBoxTovar.setSelectedIndex(-1);
        } catch (Exception ex) {
            System.out.println(ex);
            Logger.getLogger(KartaTovara.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (addOrUpdate == 1) {
            try {
                resSet = ProductRegistration.st.executeQuery("select strvozvrat.count,strvozvrat.pk_tovar"
                        + " from strvozvrat"
                        + " where strvozvrat.pk_stract=" + PK
                );
                if (resSet.next()) {
                    jSpinnerCount.setValue(Integer.parseInt(resSet.getString(1)));

                    for (int i = 0; i < pkTovar.size(); i++) {
                        if (pkTovar.get(i).equals(resSet.getString(2))) {
                            jComboBoxTovar.setSelectedIndex(i);
                            break;
                        }
                    }
                }
            } catch (Exception ex) {
                System.out.println(ex);
                Logger.getLogger(KartaTovara.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

