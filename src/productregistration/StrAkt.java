    private void jButtonOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOkActionPerformed
        // TODO add your handling code here:
        try {
            int count = 0;
            try {
                count = Integer.parseInt(jSpinnerCount.getValue().toString());
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, "Не удалось считать количество", "Сообщение", JOptionPane.INFORMATION_MESSAGE);
                System.out.println(ex);
                Logger.getLogger(KartaTovara.class.getName()).log(Level.SEVERE, null, ex);
                return;
            }
            if (addOrUpdate == 0) {
                ResultSet resSet = ProductRegistration.st.executeQuery("insert into strvozvrat(count,pk_tovar,pk_actvozvrat,pk_cost)"
                        + " values('" + count + "','" + pkTovar.get(jComboBoxTovar.getSelectedIndex()) + "','" + pkAkt + "','0')"
                );
            } else {
                ResultSet resSet = ProductRegistration.st.executeQuery("update strvozvrat set count='" + count
                        + "',pk_tovar='" + pkTovar.get(jComboBoxTovar.getSelectedIndex()) + "' where pk_stract=" + PK
                );
            }
            listenerCloseForm.event();
            this.dispose();
        } catch (Exception ex) {
            System.out.println(ex);
            Logger.getLogger(KartaTovara.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonOkActionPerformed

    private void jComboBoxTovarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxTovarActionPerformed
        // TODO add your handling code here:
        try {
            
            ResultSet resSet = ProductRegistration.st.executeQuery("select cost.zakupka,cost.nds,cost.nacenka,tovar.edizmer"
                    + " from tovar "
                    + " inner join cost on cost.PK_cost=tovar.PK_cost"
                    + " where tovar.pk_tovar=" + pkTovar.get(jComboBoxTovar.getSelectedIndex())
            );
            
            if (resSet.next()) {
                jLabelEI.setText(resSet.getString(4));
                double zakupka = Double.valueOf(resSet.getString(1));
                double nds = Double.valueOf(resSet.getString(2));
                double nacenka = Double.valueOf(resSet.getString(3));
                double snds = zakupka / 100 * nds;
                double snacenka = zakupka / 100 * nacenka;
                double itog = zakupka + snds + snacenka;
                String cost = String.format(Locale.US, "%.2f", itog);
                double summa = Double.valueOf(cost) * Double.valueOf(jSpinnerCount.getValue().toString());

                String sum = String.format(Locale.US, "%.2f", summa);
                jTextFieldCost.setText(sum);
            }
        } catch (Exception ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_jComboBoxTovarActionPerformed

